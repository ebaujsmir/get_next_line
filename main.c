#include <string.h>
#include <fcntl.h>
#include <stdio.h>

#include "get_next_line.h"

int			main(void)
{
	int		fd;
	char	*line;

	line = NULL;
	fd = open("texte1.txt", O_RDONLY);
	line = get_next_line(fd);
	while (line)
	{
		printf("%s", line);
		free(line);
		line = get_next_line(fd);
	}
	free(line);
	close(fd);
	return(0);
}
